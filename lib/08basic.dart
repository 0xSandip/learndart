void main() {
  for (int i = 1; i <= 5; i++) {
    print('hello world');
  }

  int no = 40;

  do {
    print('helloworld is $no');
    no++;
  } while (no < 50);
  // all lest one

  while (no > 50) {
    print('while');
    no++;
  }
  // only condition specific
}


