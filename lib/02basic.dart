void main() {
  String name = "raman";

// var and dynamic
  var subject = "maths";

  // subject = 7;

// here we can't use subject = 7 because var keyword automatically assigns subject with string data type when it is first declared with value maths implicitly.

// but this can be done
  var sub;
  // dynamic sub;

  sub = 'maths';
  sub = '7';
  sub = 'true';

  // this means it is dynamic and it is done when it is not initialized at first with a particular data type.
// or we can use dynamic keyword
}
